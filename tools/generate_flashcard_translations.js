// Build Map
const TERM_MAP_OBJ = {};
([...Object.entries(TERM_MAP), ...PATTERN_REPLACEMENTS]).forEach(([k, v]) => {
  TERM_MAP_OBJ[k] = v;
});

// 排序：长匹配优先
const TERM_KEYS = Object.keys(TERM_MAP_OBJ).sort((a, b) => b.length - a.length);

/**
 * 日文→中文术语替换翻译
 */
function applyTermTranslation(text) {
  if (!text || typeof text !== 'string') return text;
  let result = text;
  for (const key of TERM_KEYS) {
    const escaped = key.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
    const regex = new RegExp(escaped, 'g');
    result = result.replace(regex, TERM_MAP_OBJ[key]);
  }
  return result;
}

/**
 * 清理 HTML 标签
 */
function stripHtml(html) {
  if (!html || typeof html !== 'string') return '';
  return html
    .replace(/<br\s*\/?>/gi, '\n')
    .replace(/<\/p>/gi, '\n')
    .replace(/<\/div>/gi, '\n')
    .replace(/<[^>]+>/g, '')
    .replace(/&nbsp;/g, ' ')
    .replace(/&lt;/g, '<')
    .replace(/&gt;/g, '>')
    .replace(/&amp;/g, '&')
    .replace(/&quot;/g, '"')
    .replace(/&#39;/g, "'")
    .replace(/\n{3,}/g, '\n\n')
    .trim();
}

/**
 * 生成单张卡的中文翻译
 */
function translateCard(raw) {
  if (!raw || !raw.id) return null;

  const qJa = stripHtml(raw.questionJa || '');
  if (!qJa) return null;

  // 翻译题干
  const questionZh = applyTermTranslation(qJa);

  // 翻译选项
  const options = [];
  if (Array.isArray(raw.options)) {
    for (const o of raw.options) {
      const ja = stripHtml(o.textJa || o.text || '');
      options.push({
        textZh: ja ? applyTermTranslation(ja) : ''
      });
    }
  }

  // 翻译解析（如果有已有的 explanations_zh 更好）
  const eJa = stripHtml(raw.explanationJa || '');
  const explanationZh = eJa ? applyTermTranslation(eJa) : '';

  // 助记翻译
  const mnemonicJa = raw.mnemonicJa || '';
  const mnemonicZh = mnemonicJa ? applyTermTranslation(mnemonicJa) : '';

  return {
    questionZh,
    options,
    explanationZh,
    mnemonicZh
  };
}

// =========================================================================
// 主流程
// =========================================================================
function main() {
  let totalCards = 0;
  let totalTranslated = 0;

  for (const pkg of PACKAGES) {
    const pkgDir = path.join(ROOT, pkg.dir);
    const questionsPath = path.join(pkgDir, 'data', 'questions.js');
    const translationsPath = path.join(pkgDir, 'data', 'translations_zh.js');

    if (!fs.existsSync(questionsPath)) {
      console.warn(`[SKIP] ${pkg.dir} questions.js not found`);
      continue;
    }

    const questionsModule = require(questionsPath);
    const qby = questionsModule.questionsByYear || {};
    const years = Object.keys(qby);
    let pkgTotal = 0;
    let pkgTranslated = 0;
    const result = {};

    for (const yearId of years) {
      const cards = qby[yearId] || [];
      for (const card of cards) {
        pkgTotal++;
        totalCards++;
        const t = translateCard(card);
        if (t && t.questionZh && t.questionZh.length > 10) {
          result[card.id] = t;
          pkgTranslated++;
          totalTranslated++;
        }
      }
    }

    // 写文件
    const content = '// 由 tools/generate_flashcard_translations.js 自动生成\n' +
      '// 基于日文术语映射 + 句法模式替换，未覆盖时回退日文\n' +
      '// 每张卡包含：questionZh, options[].textZh, explanationZh, mnemonicZh\n' +
      'module.exports = ' + JSON.stringify(result, null, 2) + ';\n';

    fs.writeFileSync(translationsPath, content, 'utf-8');
    console.log(`[OK] ${pkg.dir}: ${pkgTranslated}/${pkgTotal} cards translated → translations_zh.js`);
  }

  console.log(`\n=== Summary ===`);
  console.log(`Total cards processed: ${totalCards}`);
  console.log(`Total translated: ${totalTranslated}`);
  console.log(`Coverage: ${totalCards > 0 ? Math.round(totalTranslated / totalCards * 100) : 0}%`);
}

main();
