function resolveCanonical(legacyTerm) {
  var ja = legacyTerm.termJa || '';
  var eng = legacyTerm.english || ja;
  var id = englishAliases[eng] || englishAliases[ja];
  if (id && canonicalMap[id]) {
    var c = canonicalMap[id];
    return {id:id, en:c.en, ja:c.ja, zh:c.zh,
      definitionJa:'', definitionZh:'', contextJa:'', contextZh:'',
      examCueJa:'', examCueZh:'', sourceRefs:legacyTerm.sourceRefs||[],
      isRenderable:true, isCanonical:true};
  }
  if (canonicalMap[ja]) {
    var d = canonicalMap[ja];
    return {id:ja, en:d.en, ja:d.ja, zh:d.zh,
      definitionJa:'', definitionZh:'', contextJa:'', contextZh:'',
      examCueJa:'', examCueZh:'', sourceRefs:legacyTerm.sourceRefs||[],
      isRenderable:true, isCanonical:true};
  }
  if (knownNonTerms[ja]) return null;
  return null;
}

function resolveDisplayTerms(unit) {
  var terms = (unit && unit.keyTerms) || [];
  var resolved = [], unresolved = [];
  terms.forEach(function(t) {
    var r = resolveCanonical(t);
    if (r) resolved.push(r);
    else unresolved.push(t.termJa || t.termZh || 'unknown');
  });
  return {terms:resolved, unresolvedRefs:unresolved, status:unresolved.length===0?'ready':'partial'};
}

function getTermById(termId) {
  var c = canonicalMap[termId];
  if (c) return {id:termId, en:c.en, ja:c.ja, zh:c.zh,
    definitionJa:'', definitionZh:'', contextJa:'', contextZh:'',
    examCueJa:'', examCueZh:'', sourceRefs:[]};
  return null;
}

