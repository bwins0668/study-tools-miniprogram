/**
 * scripts/setup-git-hooks.js
 * Study Tools Miniprogram - Git hooks 安装脚本
 * 在 npm prepare 阶段自动运行，将 pre-commit hook 写入 .git/hooks/
 */

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const HOOK_PATH = path.join(ROOT, '.git', 'hooks', 'pre-commit');
const SMOKE_TEST_REL = path.join('tools', 'miniprogram_smoke_test.js');
const SMOKE_TEST_ABS = path.join(ROOT, SMOKE_TEST_REL);

// 验证工具脚本是否存在
if (!fs.existsSync(SMOKE_TEST_ABS)) {
  console.error('ERROR: smoke test script not found at ' + SMOKE_TEST_ABS);
  process.exit(1);
}

// 验证 .git/hooks 目录是否存在
const hooksDir = path.join(ROOT, '.git', 'hooks');
if (!fs.existsSync(hooksDir)) {
  console.error('ERROR: .git/hooks directory not found at ' + hooksDir);
  console.error('Are you sure this is a Git repository root?');
  process.exit(1);
}

// pre-commit hook 内容
// 使用 Node.js 执行 smoke test，失败时以非 0 退出码阻止 commit
const hookContent = `#!/bin/sh
# Study Tools Miniprogram - pre-commit hook
# Auto-generated by scripts/setup-git-hooks.js
# 每次 commit 前自动运行 smoke test，失败则阻断提交

echo ""
echo "=== pre-commit smoke test ==="
SMOKE="\\"${SMOKE_TEST_ABS.replace(/\\/g, '/')}\\""
node ${SMOKE_TEST_REL.replace(/\\/g, '/')}
EXIT_CODE=$?
if [ $EXIT_CODE -ne 0 ]; then
  echo ""
  echo "===================================================="
  echo "ERROR: pre-commit smoke test FAILED (exit code: $EXIT_CODE)"
  echo "Commit aborted. Fix the issues above and try again."
  echo "===================================================="
  exit 1
fi
echo "=== pre-commit smoke test PASSED ==="
exit 0
`;

try {
  fs.writeFileSync(HOOK_PATH, hookContent, { mode: 0o755 });
  console.log('[setup-git-hooks] pre-commit hook installed at: ' + HOOK_PATH);
} catch (err) {
  console.error('ERROR: failed to write pre-commit hook at ' + HOOK_PATH);
  console.error(err.message);
  process.exit(1);
}
